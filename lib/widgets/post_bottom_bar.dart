Rồi lão bắt đầu nghi ngại cho con cá lớn mà lão đã câu. Nó tuyệt vời, kỳ lạ và tuổi tác của nó là sao nhỉ, lão nghĩ. Chưa bao giờ mình gặp một con cá nào kiêu hùng và hành động quá đỗi kỳ lạ như nó. Có lẽ nó cũng thừa khôn ngoan để không nhảy lên. Bởi nếu cứ phóng lên hoặc lao chạy thì nó có thể hủy hoại mình. Nhưng chắc trước kia cu cậu đã nhiều lần nếm mùi dây câu nên đã biết đây là cách chiến đấu tốt nhất. Nó không thể biết chỉ có mỗi một người duy nhất đang đương đầu với nó và cũng chẳng hay rằng đấy là một lão già. Nhưng cu cậu đúng là chú cá vĩ đại và sự vĩ đại ấy còn được thể hiện ở phản thịt nơi chợ nếu thịt nó hãy còn tươi khi được mang đến đó. Cu cậu đớp mồi hệt một trang nam tử và cái cách cu cậu kéo cũng là của đấng hào hoa; cu cậu chống cự không một chút hoảng sợ. Ta phân vân chẳng biết nó có mưu đồ gì không hay cũng chỉ cố gỡ gạc như cái thân của ta thôi?



Lão nhớ lần lão câu được một con trong cặp cá kiếm. Con đực luôn để con cái ăn trước và khi bị dính câu, con cái giãy giụa kinh hoàng, tuyệt vọng vẫy vùng thoát thân mạnh đến nỗi chẳng mấy chốc đã kiệt sức, suốt thời gian đó, con đực luôn ở bên bạn, bơi qua bơi lại dưới sợi dây và lượn tròn cùng bạn trên mặt nước. Nó bơi gần đến nỗi ông lão sợ nó có thể tiện phăng sợi dây câu bằng cái đuôi có hình dáng, kích cỡ và sắc tựa lưỡi hái. Khi ông lão móc con cá cái, giữ chặt cái kiếm xù xì tựa giấy nhám của nó và vung chày quật vào đỉnh đầu cho đến khi da nó chuyển sang màu như màu thủy ngân tráng phía sau gương. Và rồi với sự trợ giúp của thằng bé, lão lôi con cá lên thuyền trong lúc con cá đực vẫn cứ sán lại bên mạn thuyền. Rồi trong lúc lão đang rửa sợi dây câu và xem xét lại cây lao, con cá đực tung mình nhảy vọt lên lửng lơ trong không trung bên cạnh chiếc thuyền để xem con cá cái ở đâu, rồi rơi lặn sâu xuống; đôi cánh màu đỏ hồng, tức đôi vây hai bên ngực, xòe rộng phô hết ra những đường sọc đỏ hồng rộng. Nó thật đẹp, ông lão nhớ, và nó thủy chung. Đấy là chuyện buồn nhất mà mình từng chứng kiến qua đôi cá ấy, ông lão nghĩ. Thằng bé cũng buồn và bọn mình cầu xin nó tha thứ trước khi vội xả thịt nó.



- Giá mà thằng bé ở đây! - lão nói lớn rồi trở người tì vào chỗ ván uốn cong phía trước mũi thuyền và cảm nhận được sức mạnh của con cá khổng lồ qua sợi dây lão giữ vắt qua vai, đang đều đều tiến theo hướng nó lựa chọn.



Một khi mình đã giở mẹo thì việc con cá chọn một lối thoát là rất thỏa đáng, ông lão nghĩ. Sự lựa chọn của nó là ở trong vùng nước sâu thẳm, tối om, cách xa hết thảy những lưỡi câu, cạm bẫy, lừa lọc. Còn mình thì lại chọn lối đến tìm nó ở nơi cách xa với loài người. Cách xa tất cả loài người trên trái đất. Bây giờ mình và nó dính chặt vào nhau kể từ buổi trưa. Và chẳng có ai giúp đỡ mình hoặc nó. Lẽ ra ta đừng làm ngư dân, lão nghĩ. Nhưng đấy là việc ta sinh ra để làm. Ta phải nhớ kỹ là phải ăn con cá thu sau khi trời sáng. Vào lúc gần sáng, có con gì đó đớp một trong mấy miếng mồi phía sau lão. Lão nghe tiếng phao gỗ gãy và sợi dây bắt đầu bị kéo xuống, qua mạn thuyền. Trong bóng tối, lão rút con dao trong vỏ ra, dồn hết trọng lực lên vai trái, ngã người cắt sợi dây vướng vào mấu gỗ ở mép trên mạn thuyền. Rồi lão cắt đến sợi dây gần đấy và trong bóng tối, lão nối đầu dây của các cuộn dự trữ lại. Lão nối rất điêu luyện chỉ với một bàn tay rồi giẫm chân lên giữ khi lão thắt chặt múi nối.



Bây giờ lão đã có sáu cuộn dây dự trữ, bốn cuộn từ hai cái mồi lão đã cắt bỏ và hai cuộn từ miếng mồi con cá đang tha, tất cả đã được nối vào nhau. Khi trời sáng rõ, lão nghĩ, mình sẽ xem xét miếng mồi bốn mươi sải và cũng sẽ cắt bỏ để nối những cuộn dây dự trữ. Mình sẽ bị mất hai trăm sải dây Catalan loại tốt, lưỡi câu và cả chì nữa.

Số tài sản ấy có thể được bù đắp. Nhưng cái gì sẽ đền bù nổi con cá này nếu mình câu được vài con cá khác và chúng sẽ làm đứt sợi dây kia. Giờ thì mình không rõ con cá đớp mồi kia thuộc loại gì. Có thể đấy là một con cá kiếm, một con mõm lớn hay một con cá mập. Mình không nhận ra. Mình phải rũ bỏ nó quá nhanh. Lão nói lớn:



- Giá như mình có thằng bé!



Nhưng mày không có thằng bé đâu, lão nghĩ. Mày chỉ có một thân một mình và tốt hơn là mày nên quay lại với sợi dây câu còn lại kia, tối hay không tối thì cũng phải cắt phăng nó đi rồi nối nốt hai cuộn dây dự trữ lại. Lão làm như thế. Trong bóng tối, công việc quả khó khăn và một dạo con cá lồng lên kéo lão ngã sấp mặt xuống, đứt một vệt bên dưới mắt. Máu rỉ xuống má lão. Nhưng nó đông lại và khô trước khi bò đến cằm, lão lần về phía mũi thuyền, tựa lưng vào mạn. Lão chỉnh lại cái bao và thận trọng nhích sợi dây để nó hằn sang vùng vai khác, giữ và dùng vai neo chặt; lão cẩn thận dò sức kéo của con cá rồi nhúng tay xuống nước xem tốc độ thuyền. Mình không rõ nó nhảy lên để làm gì, lão nghĩ. Đoạn dây thép đáy hẳn quét trên mỏm lưng đồ sộ của nó. Chắc chắn lưng nó không thể đau bằng lưng mình đâu. Nhưng nó không thể cứ kéo mãi con thuyền như thế được dẫu cho nó khổng lồ đến mức nào. Giờ thì mình đã dọn sạch mọi thứ vướng víu và mình lại có cả đống dây dự trữ; con người ta chỉ cần có thế thôi.



- Cá này! - lão dịu giọng nói, - tao sẽ cầm cự với mày cho đến chết!



Nó cũng sẽ cầm cự với mình, mình chắc thế, ông lão nghĩ rồi đợi trời sáng.



Giờ đây trước khi trời sáng, không khí lạnh lẽo, lão nép sát mạn thuyền tìm hơi ấm. Mình có thể chịu đựng lâu như nó, lão nghĩ. Và trong ánh bình minh, sợi dây câu kéo dài, chếch đâm xuống nước. Con thuyền vẫn một mực thẳng tiến và khi mặt trời hé lên, tia nắng đầu tiên đậu lên vai phải lão.



- Nó bơi về phương Bắc! - ông lão nói.



Dòng nước sẽ cuốn ta dạt xa về phương đông, lão nghĩ. Mình mong nó xuôi theo dòng nước. Lúc ấy chứng tỏ nó đã thấm mệt. Khi mặt trời lên cao hơn, ông lão nhận thấy con cá vẫn chưa mệt. Chỉ có một dấu hiệu đáng mừng duy nhất. Độ chếch của sợi dây cho thấy nó không còn bơi sâu nữa. Dấu hiệu ấy không thực sự có nghĩa con cá sẽ nhảy lên. Nhưng nó có thể.



- Cầu Chúa làm nó nhảy lên! - ông lão nói. - Mình còn đủ dây để chinh phục nó!



Có lẽ nếu ta kéo căng dây một tí thì nó sẽ bị đau và nhảy lên, lão nghĩ. Bây giờ đã ban ngày ban mặt, nó cứ nhảy lên đi để những chiếc túi dọc theo xương sống chứa đầy không khí khiến nó không thể lặn sâu xuống chết. Lão cố kéo, nhưng sợi dây vẫn cứ căng đến mức chuẩn bị đứt tung như từ lúc lão mới câu được con cá và khi ưỡn người ra để kéo, lão cảm nhận được sức cản ấy và biết là mình không thể dồn thêm lực vào đó. Mình không nên giật mạnh, lão nghĩ. Mỗi cú giật sẽ nới rộng thêm chỗ lưỡi câu móc vào rồi khi nhảy lên, con cá có thể tuột thoát. Dẫu sao thì mặt trời đã làm mình dễ chịu hơn và lần này thì mình không phải nhìn trực diện về phía mặt trời. Những mảng rong biển màu vàng bám vào dây câu nhưng lão biết chúng chỉ làm tăng thêm sức cản, lão hài lòng. Đấy là giống rong vàng vùng nhiệt lưu, ban đêm tỏa nhiều lân tinh.



- Cá này! - lão nói, - tao rất yêu và ngưỡng mộ mày.



Nhưng tao sẽ giết mày trước khi ngày kết thúc! Ta hãy hy vọng thế, lão nghĩ.



Từ phương Bắc, một con chim nhỏ bay về phía thuyền. Nó thuộc giống warbler và bay rất thấp trên mặt nước. Ông lão có thể nhận thấy con chim đã thấm mệt. Con chim bay đến đuôi thuyền, đậu xuống. Lát sau nó lượn vòng quanh đầu ông lão và đáp xuống sợi dây nơi nó cảm thấy thoải mái hơn.



- Mày bao nhiêu tuổi rồi?! - ông lão hỏi con chim. - Có phải đây là chuyến đi đầu tiên của mày không?!



Con chim nhìn lão khi lão nói. Nó thậm chí đã quá mệt để kiểm tra sợi dây và khi đôi chân mảnh mai đáp nhanh trên sợi dây, con chim lảo đảo.



- Nó chắc đấy! - lão bảo con chim. - Nó chắc lắm. Sau một đêm lặng gió lẽ ra mày chưa mệt mới phải. Loài chim nào sắp đến đây?!



Đám diều hâu, lão nghĩ, sẽ ra khơi săn chúng. Nhưng lão không nói điều ấy với con chim bởi làm sao mà nó có thể hiểu được lão và đâu có thể lập tức hiểu ngay được lũ diều hâu.



- Cứ nghỉ ngơi thoải mái đi, chú chim nhỏ! - lão nói. - Rồi bay vào bờ, tận hưởng vận may như bất kỳ con người, con chim hay con cá nào!



Con chim khuyến khích lão nói chuyện bởi suốt đêm lưng lão đã tê cứng còn bây giờ thì thực sự nhức nhối.



- Hãy ở lại nhà ta nếu chú mày muốn, chim à! - lão nói. - Ta lấy làm tiếc là không thể giương buồm nương theo làn gió nhẹ đang thổi đưa chú mày vào đất liền. Bởi ta đang bận tiếp một người bạn!



Đúng lúc ấy, bất thình lình con cá giật mạnh, kéo lão ngã sấp xuống mũi thuyền và suýt lôi lão xuống biển nếu lão không gồng hết sức níu giữ và nới thêm dây. Con chim bay vụt lên khi sợi dây giật mạnh và ông lão thậm chí cũng không nhìn thấy nó bay đi. Lão thận trọng đưa tay phải sờ sợi dây và thấy bàn tay mình đang chảy máu.



- Đúng là có cái gì đó làm đau nó! lão nói lớn và kéo dây lại để xem có thể lay chuyển được con cá không. Nhưng khi ráng đến điểm căng nhất thì lão đành giữ chặt và quay lại chống đỡ sức kéo của sợi dây.



- Giờ thì mày sắp nếm mùi rồi đấy, cá à! - lão nói. - Và lạy Chúa, cả tao cũng vậy!



